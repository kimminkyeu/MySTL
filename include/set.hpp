/*  From Microsoft Learn.

	allocator_type	set 개체의 allocator 클래스를 나타내는 형식입니다.
	const_iterator	set에 있는 모든 const 요소를 읽을 수 있는 양방향 반복기를 제공하는 형식입니다.
	const_pointer	set에서 const 요소에 대한 포인터를 제공하는 형식입니다.
	const_reference	읽기 및 const 작업을 수행하기 위해 const 집합에 저장된 요소에 대한 참조를 제공하는 형식입니다.
	const_reverse_iterator	set에 있는 모든 const 요소를 읽을 수 있는 양방향 반복기를 제공하는 형식입니다.
	difference_type	부호 있는 정수 형식은 반복기가 가리키는 요소 사이의 범위에 있는 set의 요소의 개수를 표현하는 데 사용할 수 있습니다.
	iterator	set에 있는 모든 요소를 읽거나 수정할 수 있는 양방향 반복기를 제공하는 형식입니다.
	key_compare	set의 두 요소간 상대적 순서를 결정하는 두 정렬 키를 비교할 수 있는 함수 개체를 제공하는 형식입니다.
	key_type	해당 용량 내 set 요소로 저장된 개체를 정렬 키로 설명하는 형식입니다.
	pointer	set에서 요소에 대한 포인터를 제공하는 형식입니다.
	reference	set에 저장된 요소에 대한 참조를 제공하는 형식입니다.
	reverse_iterator	역순 set의 요소를 읽거나 수정할 수 있는 양방향 반복기를 제공하는 형식입니다.
	size_type	set에서 요소 수를 표현할 수 있는 부호 없는 정수 형식입니다.
	value_compare	두 요소를 비교하여 set에서 상대적인 순서를 결정할 수 있는 함수 개체를 제공하는 형식입니다.
	value_type	해당 용량 내 set 요소로 저장된 개체를 값으로 설명하는 형식입니다.
*/

